public class precedencia_operadores_matematicos {
  public static void main(String[] args) {

    /*
      PRECEDÊNCA DOS OPERADORES SEM PARÊNTESES
      1° - Divisão (/)
      2° - Multiplicação (*)
      3° - Resto da divisão (%)
      4°- Adição e subtração (+ e -)
    */

    int x = 10/2*3+4*3;
    System.out.println(x);
    
  }
}