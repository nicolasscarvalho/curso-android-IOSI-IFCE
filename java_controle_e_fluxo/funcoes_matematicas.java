public class FuncoesMatematicas {
    public static void main(String[] args) {
      System.out.println("Máximo do conjunto (10, 20): " + Math.max(10, 20));
      System.out.println("Mínimo do conjunto (10, 20): " + Math.min(10,20));
      System.out.println("Raiz quadrada de 100: " + Math.sqrt(100.0));
      System.out.println("log de 100 na base 10: " + Math.log10(100));
      System.out.println("2^3: " + Math.pow(2, 3));
      System.out.println("constante Pi: " + Math.PI);
      System.out.println("Constante E: " + Math.E);
      System.out.println("Arredondando 2.3399: " + Math.round(2.3399));
      System.out.println("Número aleatório gerado: " + Math.random());
    }
}