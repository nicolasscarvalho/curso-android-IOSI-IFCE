class dados_por_referencia_e_primitivos {
  public static void main(String[] args) {
    // TIPOS POR REFERÊNCIA -> Objetos com métodos já prontos
    Byte rByte = 5;
    Short rShort = 5;
    Integer rInteger = 5;
    Long rLong = 5L;
    Float rFloat = 5.0F;
    Double rDouble = 5.0;
    Boolean rBoolean = true;
    String rString = "Java";
    Character rCharacter = 'J';


    // TIPOS PRIMITIVOS -> Apenas definem o tipo de dado
    byte pByte = 5;
    short pShort = 5;
    int pInteger = 5;
    long pLong = 5L;
    float pFloat = 5.0F;
    double pDouble = 5.0;
    boolean pBoolean = true;
    char pCharacter = 'J';
  }
}