public class operadores_matematicos {
  public static void main(String[] args) {

    System.out.println("10 + 3 = " + (10+3));
    System.out.println("10 - 3 = " + (10-3));
    System.out.println("10 * 3 = " + (10*3));
    System.out.println("10 / 3 = " + (10/3));
    System.out.println("10 % 3 = " + (10%3));

    int x = 10;
    System.out.println("x = " + x);
    x += 3;
    System.out.println("x = " + x);

    System.out.println("10.0 / 3.0 = " + (10.0/3.0));
    
  }
}